/**
 * دریافت اموجی آیتم براساس نوع
 * @param itemType نوع آیتم
 * @returns اموجی مناسب
 */
export function getItemTypeEmoji(itemType: string): string {
  const lowercaseType = itemType?.toLowerCase() || '';
  const emoji = ITEM_EMOJI[lowercaseType.toUpperCase() as keyof typeof ITEM_EMOJI];
  return emoji || '📦';
}

/**
 * دریافت اموجی نادر بودن
 * @param rarity نادر بودن
 * @returns اموجی مناسب
 */
export function getRarityEmoji(rarity: string): string {
  switch(rarity?.toLowerCase()) {
    case 'common':
      return ITEM_EMOJI.COMMON;
    case 'uncommon':
      return ITEM_EMOJI.UNCOMMON;
    case 'rare':
      return ITEM_EMOJI.RARE;
    case 'epic':
      return ITEM_EMOJI.EPIC;
    case 'legendary':
      return ITEM_EMOJI.LEGENDARY;
    case 'unique':
      return ITEM_EMOJI.UNIQUE;
    default:
      return ITEM_EMOJI.COMMON;
  }
}

