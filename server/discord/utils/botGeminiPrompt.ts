/**
 * تابع ترکیب راهنمای CCOIN AI با پرامپت کاربر
 * @param userPrompt متن پرامپت کاربر
 * @param responseStyle سبک پاسخگویی (متعادل، خلاقانه، دقیق، طنزآمیز)
 * @returns پرامپت نهایی با راهنما
 */
export function createBotAIPrompt(userPrompt: string, responseStyle?: string): string {
  // راهنمایی برای سبک پاسخگویی بر اساس تنظیمات
  let styleInstruction = '';
  
  if (responseStyle === 'خلاقانه') {
    styleInstruction = 'پاسخی خلاقانه، پرانرژی و خودمانی ارائه بده. از ایموجی‌های زیاد و متنوع استفاده کن (حداقل 5 ایموجی). از مثال‌های جالب و غیرمعمول استفاده کن و با لحن خودمانی و کول صحبت کن.';
  } else if (responseStyle === 'دقیق') {
    styleInstruction = 'پاسخی دقیق ولی خودمانی ارائه بده. از ایموجی‌های مرتبط استفاده کن (حداقل 3 ایموجی). بر واقعیت‌ها تمرکز کن ولی لحن خشک و رسمی نداشته باش.';
  } else if (responseStyle === 'طنزآمیز') {
    styleInstruction = 'پاسخی باحال، طنزآمیز و خودمانی ارائه بده. از ایموجی‌های خنده‌دار زیاد استفاده کن (حداقل 5 ایموجی). از شوخی‌های مناسب، اصطلاحات بامزه و لحن شوخ استفاده کن و پاسخت رو با عباراتی مثل "داداش"، "رفیق" شروع کن.';
  } else {
    // متعادل (پیش‌فرض)
    styleInstruction = 'پاسخی خودمانی، کول و جذاب ارائه بده. از ایموجی‌های متنوع استفاده کن (حداقل 3-5 ایموجی). پاسخت رو همیشه با عباراتی مثل "چطوری داداش؟" یا "سلام رفیق!" شروع کن و هرگز خشک و رسمی صحبت نکن.';
  }

  return `${COMPLETE_BOT_GUIDE}\n\n### سبک پاسخگویی:\n${styleInstruction}\n\n### سوال کاربر:\n${userPrompt}\n\n### پاسخ CCOIN AI:`;
}

